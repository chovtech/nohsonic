                            <h2 class="text-anime-style-3">Common Conditions in Abuja's Office Workers</h2>

                            <p class="wow fadeInUp">The combination of prolonged sitting, poor posture, and workplace stress gives rise to several musculoskeletal conditions that we see regularly at NOHSONIC:</p>

                            <ul class="wow fadeInUp">
                                <li><strong>Cervicogenic headaches:</strong> Headaches originating from tightness and joint dysfunction in the neck. Often felt as a dull ache at the base of the skull that spreads to the temples or behind the eyes.</li>
                                <li><strong>Upper cross syndrome:</strong> A pattern of muscle imbalance where the chest and upper neck muscles are overactive and tight, while the deep neck flexors and mid-back muscles are weak. This creates the classic rounded-shoulder, forward-head posture seen in so many desk workers.</li>
                                <li><strong>Lower back pain:</strong> Sustained sitting loads the lumbar discs unevenly and deactivates the core muscles, leading to pain, stiffness, and sometimes disc problems over time.</li>
                                <li><strong>Carpal tunnel syndrome:</strong> Repetitive keyboard and mouse use can compress the median nerve at the wrist, causing tingling, numbness, and weakness in the hand.</li>
                                <li><strong>Shoulder impingement:</strong> Poor posture reduces the space available for the rotator cuff tendons, making painful shoulder catching and aching common among desk workers.</li>
                                <li><strong>Text neck:</strong> The habit of looking down at a mobile phone for hours a day dramatically increases the load on the cervical spine, accelerating degeneration and causing pain in younger workers than ever before.</li>
                            </ul>

